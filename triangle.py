def area(a, b):
    '''
Area of triangle with given sides

Parameters: 
    int a,b: sides of the triangle

Return value:
    (int): area of the triangle with given sides
'''
    return a * b

def perimeter(a, b):
    '''
Area of triangle with given sides

Parameters: 
    int a,b: sides of the triangle

Return value:
    (int): area of the triangle with given sides
'''
    return a + b
